create policy profiles_select_own
on public.profiles for select to authenticated
using (id = (select auth.uid()));
create policy profiles_insert_own
on public.profiles for insert to authenticated
with check (id = (select auth.uid()));
create policy profiles_update_own
on public.profiles for update to authenticated
using (id = (select auth.uid()))
with check (id = (select auth.uid()));

create policy projects_select_member
on public.projects for select to authenticated
using (private.is_project_member(id));
create policy projects_insert_owner
on public.projects for insert to authenticated
with check (owner_id = (select auth.uid()));
create policy projects_update_editor
on public.projects for update to authenticated
using (private.can_edit_project(id))
with check (private.can_edit_project(id));
create policy projects_delete_owner
on public.projects for delete to authenticated
using (owner_id = (select auth.uid()));

create policy members_select_member
on public.project_members for select to authenticated
using (
  private.is_project_member(project_id)
  or user_id = (select auth.uid())
  or (
    status = 'pending'
    and lower(invited_email) =
      lower(coalesce((select auth.jwt()) ->> 'email', ''))
  )
);
create policy members_insert_owner
on public.project_members for insert to authenticated
with check (
  exists (
    select 1
    from public.projects p
    where p.id = project_id
      and p.owner_id = (select auth.uid())
  )
);
create policy members_update_owner
on public.project_members for update to authenticated
using (
  exists (
    select 1
    from public.projects p
    where p.id = project_id
      and p.owner_id = (select auth.uid())
  )
)
with check (
  exists (
    select 1
    from public.projects p
    where p.id = project_id
      and p.owner_id = (select auth.uid())
  )
);
create policy members_delete_owner
on public.project_members for delete to authenticated
using (
  exists (
    select 1
    from public.projects p
    where p.id = project_id
      and p.owner_id = (select auth.uid())
  )
  and role <> 'owner'
);

create policy revisions_select_member
on public.project_revisions for select to authenticated
using (private.is_project_member(project_id));
create policy revisions_insert_editor
on public.project_revisions for insert to authenticated
with check (
  private.can_edit_project(project_id)
  and created_by = (select auth.uid())
);

create policy activity_select_member
on public.project_activity for select to authenticated
using (private.is_project_member(project_id));
create policy activity_insert_editor
on public.project_activity for insert to authenticated
with check (
  private.can_edit_project(project_id)
  and actor_id = (select auth.uid())
);

