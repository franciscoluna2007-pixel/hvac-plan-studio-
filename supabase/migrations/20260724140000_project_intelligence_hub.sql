for each row execute function public.prepare_project_work_item();

create or replace function public.prepare_project_approval()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
begin
  if old.status <> 'requested' then
    raise exception 'Approval decisions are final';
  end if;
  if new.status = 'requested' then
    raise exception 'Choose approved or changes requested';
  end if;
  if new.status = 'changes_requested' and nullif(trim(new.decision_note), '') is null then
    raise exception 'A note is required when requesting changes';
  end if;
  new.decided_at = now();
  return new;
end;
$$;

