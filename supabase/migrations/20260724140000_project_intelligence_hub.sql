create or replace function public.save_project_revision(
  project_uuid uuid,
  revision_title text,
  revision_summary text,
  revision_snapshot jsonb,
  revision_drawing_count integer
)
returns public.project_revisions
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  next_revision bigint;
  created_revision public.project_revisions;
  next_workflow_summary jsonb;
begin
  if auth.uid() is null or not private.can_edit_project(project_uuid) then
    raise exception 'You do not have permission to save this project';
  end if;

  perform pg_advisory_xact_lock(hashtextextended(project_uuid::text, 0));
  select coalesce(max(revision_number), 0) + 1
  into next_revision
  from public.project_revisions
  where project_id = project_uuid;

  next_workflow_summary := coalesce(revision_snapshot -> 'workflowSummary', '{}'::jsonb);

  insert into public.project_revisions(
    project_id,
    revision_number,
    created_by,
    title,
    summary,
    snapshot,
    drawing_count,
    workflow_summary,
    content_hash
  )
  values (
    project_uuid,
    next_revision,
    auth.uid(),
    coalesce(nullif(trim(revision_title), ''), 'Revision ' || next_revision),
    coalesce(trim(revision_summary), ''),
    revision_snapshot,
    greatest(coalesce(revision_drawing_count, 0), 0),
    next_workflow_summary,
    encode(extensions.digest(revision_snapshot::text, 'sha256'), 'hex')
  )
  returning * into created_revision;

  update public.projects
  set workflow_summary = next_workflow_summary,
      updated_at = now()
  where id = project_uuid;

  return created_revision;
end;
$$;

create or replace function public.record_drive_package_sync(
  project_uuid uuid,
  package_file_id text,
  package_file_url text,
  synced_revision bigint
)
returns public.projects
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  updated_project public.projects;
  synced_revision_id uuid;
  package_name text;
begin
  if auth.uid() is null or not private.can_edit_project(project_uuid) then
    raise exception 'You do not have permission to sync this project';
  end if;

  if nullif(trim(package_file_id), '') is null or synced_revision < 1 then
    raise exception 'A valid Drive file and cloud revision are required';
  end if;

  select id
  into synced_revision_id
  from public.project_revisions
  where project_id = project_uuid
    and revision_number = synced_revision;

  if synced_revision_id is null then
    raise exception 'The selected cloud revision does not exist';
  end if;

  update public.projects
  set drive_package_file_id = package_file_id,
      drive_package_url = nullif(trim(package_file_url), ''),
      drive_synced_revision_number = synced_revision,
      drive_synced_at = now()
  where id = project_uuid
  returning * into updated_project;

  if updated_project.id is null then
    raise exception 'Project not found';
  end if;
  package_name := coalesce(nullif(trim(updated_project.name), ''), 'HVAC Plan Studio') || '.hvacplan.json';

  insert into public.project_files(
    project_id,
    revision_id,
    kind,
    provider,
    provider_file_id,
    display_name,
    mime_type,
    web_view_url,
    status,
    created_by
  )
  values (
    project_uuid,
    synced_revision_id,
    'cloud_package',
    'google_drive',
    package_file_id,
    package_name,
    'application/json',
    nullif(trim(package_file_url), ''),
    'active',
    auth.uid()
  )
  on conflict (project_id, provider, provider_file_id)
  do update set
    revision_id = excluded.revision_id,
    display_name = excluded.display_name,
    mime_type = excluded.mime_type,
    web_view_url = excluded.web_view_url,
    status = 'active';

  insert into public.project_activity(project_id, actor_id, action, details)
  values (
    project_uuid,
    auth.uid(),
    'drive_package_synced',
    jsonb_build_object(
      'revision', synced_revision,
      'drive_file_id', package_file_id
    )
  );

  return updated_project;
end;
$$;

create or replace function public.set_project_status(
  project_uuid uuid,
  next_status text
)
returns public.projects
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  updated_project public.projects;
begin
  if auth.uid() is null then
    raise exception 'Authentication required';
  end if;
  if next_status not in ('active', 'archived') then
    raise exception 'Invalid project status';
  end if;
  if not exists (
    select 1 from public.projects
    where id = project_uuid and owner_id = auth.uid()
  ) then
    raise exception 'Only the project owner can change project status';
  end if;

  update public.projects
  set status = next_status
  where id = project_uuid
  returning * into updated_project;

  insert into public.project_activity(project_id, actor_id, action, details)
  values (
    project_uuid,
    auth.uid(),
    'project_status_changed',
    jsonb_build_object('status', next_status)
  );

  return updated_project;
end;
$$;

