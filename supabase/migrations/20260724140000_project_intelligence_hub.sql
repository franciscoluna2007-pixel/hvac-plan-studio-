create or replace function public.log_project_intelligence_activity()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  activity_action text;
  activity_project uuid;
  activity_details jsonb;
begin
  activity_project := new.project_id;
  if tg_table_name = 'project_work_items' then
    activity_action := case when tg_op = 'INSERT' then 'work_item_created' else 'work_item_updated' end;
    activity_details := jsonb_build_object(
      'work_item_id', new.id,
      'kind', new.kind,
      'status', new.status,
      'priority', new.priority,
      'title', new.title
    );
  elsif tg_table_name = 'project_comments' then
    activity_action := 'work_item_commented';
    activity_details := jsonb_build_object('work_item_id', new.work_item_id);
  elsif tg_table_name = 'project_approvals' then
    activity_action := case when tg_op = 'INSERT' then 'approval_requested' else 'approval_decided' end;
    activity_details := jsonb_build_object(
      'approval_id', new.id,
      'revision_id', new.revision_id,
      'status', new.status
    );
  else
    activity_action := case when tg_op = 'INSERT' then 'project_file_registered' else 'project_file_updated' end;
    activity_details := jsonb_build_object(
      'file_id', new.id,
      'kind', new.kind,
      'display_name', new.display_name
    );
  end if;

  insert into public.project_activity(project_id, actor_id, action, details)
  values (activity_project, auth.uid(), activity_action, activity_details);

  update public.projects set updated_at = now() where id = activity_project;
  return new;
end;
$$;

